var nav = document.querySelector('nav');
window.addEventListener('scroll',function(){
  if(window.pageYOffset>800){
    nav.classList.add('bg-dark');
  }else{
   nav.classList.remove('bg-dark');
  }
});

$(document).ready(function(){
  $('.card1').hover(
    function(){
      $(this).animate({
        marginTop: "-=1%",
      },100);
    },
    function(){
      $(this).animate({
        marginTop: "0%"
      },100);
    }
  );
})
